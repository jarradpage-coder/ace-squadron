import Phaser from 'phaser'

// All art is generated at runtime — no asset files, fully offline, zero IP risk.
export function createTextures(scene: Phaser.Scene): void {
  if (scene.textures.exists('player')) return // already built (e.g. on restart)

  const g = scene.add.graphics()

  // Player plane — cream, pointing up (30x34)
  g.clear()
  g.fillStyle(0xf2e2b6)
  g.fillTriangle(15, 0, 4, 24, 26, 24)
  g.fillRect(1, 19, 28, 6)
  g.fillRect(11, 22, 8, 12)
  g.fillStyle(0xb23b3b)
  g.fillCircle(15, 13, 3)
  g.generateTexture('player', 30, 34)

  // Wingman escort — grey (18x18)
  g.clear()
  g.fillStyle(0xcdd6da)
  g.fillTriangle(9, 0, 1, 14, 17, 14)
  g.fillRect(1, 11, 16, 4)
  g.generateTexture('wingman', 18, 18)

  // Enemy fighter — red, pointing down (26x26)
  g.clear()
  g.fillStyle(0xe24b4a)
  g.fillTriangle(13, 26, 3, 4, 23, 4)
  g.fillRect(1, 4, 24, 6)
  g.fillStyle(0x3a0c0c)
  g.fillCircle(13, 13, 3)
  g.generateTexture('enemy', 26, 26)

  // Enemy fighter — grey variant (non-red waves) (26x26)
  g.clear()
  g.fillStyle(0x8a9bb0)
  g.fillTriangle(13, 26, 3, 4, 23, 4)
  g.fillRect(1, 4, 24, 6)
  g.fillStyle(0x101c28)
  g.fillCircle(13, 13, 3)
  g.generateTexture('enemyGrey', 26, 26)

  // Player bullet — yellow (6x16)
  g.clear()
  g.fillStyle(0xffe14d)
  g.fillRoundedRect(0, 0, 6, 16, 3)
  g.generateTexture('pbullet', 6, 16)

  // Enemy bullet — orange (10x10)
  g.clear()
  g.fillStyle(0xffb347)
  g.fillCircle(5, 5, 5)
  g.fillStyle(0xff7b29)
  g.fillCircle(5, 5, 2.5)
  g.generateTexture('ebullet', 10, 10)

  // Power-up capsule — white so it can be tinted per POW type (26x26)
  g.clear()
  g.fillStyle(0xffffff)
  g.fillRoundedRect(0, 0, 26, 26, 7)
  g.lineStyle(2, 0x0b1f3a)
  g.strokeRoundedRect(1, 1, 24, 24, 7)
  g.generateTexture('pow', 26, 26)

  // Boss bomber — dark, wide (200x96)
  g.clear()
  g.fillStyle(0x394b5a)
  g.fillRoundedRect(0, 26, 200, 52, 10)
  g.fillStyle(0x2b3947)
  g.fillRoundedRect(72, 0, 56, 96, 14)
  g.fillStyle(0x101820)
  g.fillCircle(42, 52, 8)
  g.fillCircle(158, 52, 8)
  g.fillCircle(74, 60, 7)
  g.fillCircle(126, 60, 7)
  g.fillStyle(0xe24b4a)
  g.fillRect(94, 10, 12, 20)
  g.generateTexture('boss', 200, 96)

  // Explosion particle — white square (6x6)
  g.clear()
  g.fillStyle(0xffffff)
  g.fillRect(0, 0, 6, 6)
  g.generateTexture('particle', 6, 6)

  // --- Terrain (scrolling background scenery) ---

  // Large island / atoll
  g.clear()
  g.fillStyle(0xc9a96b)
  g.fillEllipse(42, 35, 84, 64)
  g.fillStyle(0x3f7d4a)
  g.fillEllipse(42, 33, 62, 44)
  g.fillStyle(0x2c6138)
  g.fillEllipse(39, 31, 34, 24)
  g.generateTexture('island1', 84, 70)

  // Small atoll with a lagoon
  g.clear()
  g.fillStyle(0xc9a96b)
  g.fillEllipse(30, 27, 56, 48)
  g.fillStyle(0x3f7d4a)
  g.fillEllipse(30, 26, 40, 32)
  g.fillStyle(0x2f6f9a)
  g.fillEllipse(31, 26, 16, 12)
  g.generateTexture('island2', 60, 54)

  // Destroyer (top-down): triangular bow + rounded hull, with a wake
  g.clear()
  g.fillStyle(0xffffff, 0.16)
  g.fillTriangle(11, 80, 2, 66, 20, 66) // wake astern
  g.fillStyle(0x76828f)
  g.fillTriangle(11, 0, 3, 17, 19, 17)
  g.fillRoundedRect(3, 14, 16, 52, 5)
  g.fillStyle(0x9aa6b4)
  g.fillRect(7, 28, 8, 16)
  g.fillStyle(0x3a4350)
  g.fillRect(9, 9, 4, 7)
  g.generateTexture('ship', 22, 82)

  // Carrier (top-down, flight deck)
  g.clear()
  g.fillStyle(0x5b6474)
  g.fillRoundedRect(2, 0, 34, 118, 6)
  g.fillStyle(0x39414c)
  g.fillRect(18, 8, 2, 102)
  g.fillStyle(0xd7c98a)
  g.fillRect(15, 6, 8, 3)
  g.fillRect(15, 109, 8, 3)
  g.fillStyle(0x6b7682)
  g.fillRect(28, 42, 8, 22)
  g.generateTexture('carrier', 38, 120)

  g.destroy()
}
